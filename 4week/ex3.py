def sum_int(a,b):
    return a+b
#정의 하는 부분이 있어야 호출이 가능
num1=10
num2=20
result=sum_int(num1,num2)
print(f"{num1}+{num2}={result}")
print("------------------------------------------------")
def s_int(a,b):
    return a*b
#정의 하는 부분이 있어야 호출이 가능
num1=10
num2=20
re=s_int(num1,num2)
print(f"{num1}*{num2}={re}")